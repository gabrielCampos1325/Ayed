package ElemMaxProfPostOrden;

public class Resultado {
	int valor;
	int profMax;
	
	public Resultado() {
		this.valor = 0;
		this.profMax = 0;
	}
	
	public int getValor() {
		return valor;
	}
	public void setValor(int valor) {
		this.valor = valor;
	}
	public int getProfMax() {
		return profMax;
	}
	public void setProfMax(int profMax) {
		this.profMax = profMax;
	}
}
